package org;

public class PrePostDemo {

	public static void main(String[] args) {
		int i = 3;
		i++;

		// print4
		System.out.println(i);

		++i;
        // print5
		System.out.println(i);

		// print6
		System.out.println(++i);

		// print6
		System.out.println(i++);

		// print7
		System.out.println(i);

	}

}
