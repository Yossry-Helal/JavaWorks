package org;

public class LogicalOperator {

	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;

		if ((value1 == 1) && (value2 == 2)) {
			System.out.println("Value 1 is  1 AND Value 2 == 2");
		}
		if ((value1 == 1) || (value2 == 1)) {
			System.out.println("Value 1 is 1 OR Value 2 == 1");

		}
	}
}
