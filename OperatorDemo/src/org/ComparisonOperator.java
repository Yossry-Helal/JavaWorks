package org;

public class ComparisonOperator {

	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;

		if (value1 == value2) {
			System.out.println("Value 1 == Value 2");
		}
		if (value1 != value2) {
			System.out.println("Value 1 != Value 2");
		}
		if (value1 > value2) {
			System.out.println("Value 1 > Value 2");
		}
		if (value1 < value2) {
			System.out.println("Value 1 < Value 2");
		}

		if (value1 <= value2) {
			System.out.println("Value 1 <= Value 2");
		}

	}

}
